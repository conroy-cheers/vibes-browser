import fs from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const PROMPT_FILES = [
  {
    exportName: 'DEFAULT_SESSION_PLANNER_PROMPT',
    sourcePath: 'system-prompt.md',
  },
  {
    exportName: 'DEFAULT_RENDERER_PAGE_PROMPT',
    sourcePath: 'renderer-page-prompt.md',
  },
  {
    exportName: 'DEFAULT_RENDERER_SCAFFOLDING',
    sourcePath: 'renderer-scaffolding.md',
  },
];

export async function syncSystemPrompt(
  root = path.resolve(import.meta.dirname, '..'),
) {
  const outputPath = path.join(root, 'src', 'prompt-defaults.generated.mjs');
  const generated = [
    '// Generated from markdown prompt files by scripts/sync-system-prompt.mjs.',
    '// Do not edit this file directly.',
    '',
  ];

  for (const promptFile of PROMPT_FILES) {
    const markdown = await fs.readFile(
      path.join(root, promptFile.sourcePath),
      'utf8',
    );
    generated.push(...renderExportBlock(promptFile.exportName, markdown));
  }

  await fs.writeFile(outputPath, generated.join('\n'), 'utf8');
}

const isEntrypoint =
  process.argv[1] &&
  path.resolve(process.argv[1]) ===
    path.resolve(fileURLToPath(import.meta.url));
if (isEntrypoint) {
  await syncSystemPrompt();
}

function renderExportBlock(exportName, markdown) {
  return [
    `export const ${exportName} = [`,
    ...markdown.split('\n').map((line) => `  ${toSingleQuotedLiteral(line)},`),
    "].join('\\n');",
    '',
  ];
}

function toSingleQuotedLiteral(value) {
  const singleQuoted = quoteWith(value, "'");
  const doubleQuoted = quoteWith(value, '"');

  const singleEscapes = countEscapes(singleQuoted);
  const doubleEscapes = countEscapes(doubleQuoted);

  if (doubleEscapes < singleEscapes) {
    return doubleQuoted;
  }

  return singleQuoted;
}

function quoteWith(value, quote) {
  const escapeQuote = quote === "'" ? /'/g : /"/g;
  return `${quote}${value
    .replaceAll('\\', '\\\\')
    .replace(escapeQuote, `\\${quote}`)
    .replaceAll('\r', '\\r')
    .replaceAll('\n', '\\n')}${quote}`;
}

function countEscapes(value) {
  return [...value].filter((character, index) => {
    return character === '\\' && value[index + 1] !== 'n';
  }).length;
}
